package com.transfomation.helper;
/**
 * This class contains details of Winner when found. 
 *  
 * @author anandsoni
 *
 */

public class WinnerDetails {
	
	private boolean foundWinner;
	private String winner;
	
	
	public boolean isFoundWinner() {
		return foundWinner;
	}
	public void setFoundWinner(boolean foundWinner) {
		this.foundWinner = foundWinner;
	}
	public String getWinner() {
		return winner;
	}
	public void setWinner(String winner) {
		this.winner = winner;
	}
	

}
